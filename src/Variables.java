class Variables {
	
	public static void main (String args[]) {
		
		int N = 3;
		double A = 4.56;
		 
		System.out.println("El resultado de N, A y caracter es:"); //Muestro un mensaje de aviso.
		System.out.println(N+A); //resultado de la suma.
		System.out.println(N-A); //resultado de la resta.	
		System.out.println(N*A); //resultado de la multiplicación.
		System.out.println(N/A); //resultado de la división.	
		System.out.println(N%A); //resultado de la división.
	}
	
}

		